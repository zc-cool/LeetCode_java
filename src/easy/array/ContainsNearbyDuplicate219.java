public class ContainsNearbyDuplicate219 {
    public boolean containsNearbyDuplicate(int[] nums, int k) {
        for (int i = 0; i < nums.length; i++) {
            for(int j = i; j < nums.length && j <= i + k; j++){
                if(nums[i] == nums[j] && i != j){
                    return true;
                }
            }
        }
        return false;
    }
}
